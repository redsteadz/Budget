<?php

declare(strict_types=1);

namespace OCA\Budget\Service;

use OCA\Budget\Db\AccountMapper;
use OCA\Budget\Db\BillMapper;
use OCA\Budget\Db\TransactionMapper;
use OCA\Budget\Service\Bill\FrequencyCalculator;
use OCA\Budget\Service\MoneyCalculator;

/**
 * Diagnoses and repairs data integrity issues.
 */
class RepairService {
    private TransactionMapper $transactionMapper;
    private BillMapper $billMapper;
    private AccountMapper $accountMapper;
    private FrequencyCalculator $frequencyCalculator;
    private AccountService $accountService;

    public function __construct(
        TransactionMapper $transactionMapper,
        BillMapper $billMapper,
        AccountMapper $accountMapper,
        FrequencyCalculator $frequencyCalculator,
        AccountService $accountService
    ) {
        $this->transactionMapper = $transactionMapper;
        $this->billMapper = $billMapper;
        $this->accountMapper = $accountMapper;
        $this->frequencyCalculator = $frequencyCalculator;
        $this->accountService = $accountService;
    }

    /**
     * Diagnose data integrity issues without making any changes.
     *
     * @return array Categorized findings
     */
    public function diagnose(string $userId): array {
        return [
            'duplicateTransactions' => $this->findDuplicateAutoGenerated($userId),
            'stuckBills' => $this->findStuckBills($userId),
            'futureClearedTransactions' => $this->findFutureClearedTransactions($userId),
            'balanceDrift' => $this->findBalanceDrift($userId),
        ];
    }

    /**
     * Repair specific categories of issues.
     *
     * @param string[] $categories Which categories to fix: 'duplicateTransactions', 'stuckBills', 'balanceDrift'
     * @return array Results per category
     */
    public function repair(string $userId, array $categories): array {
        $results = [];

        if (in_array('duplicateTransactions', $categories)) {
            $results['duplicateTransactions'] = $this->repairDuplicateAutoGenerated($userId);
        }

        if (in_array('stuckBills', $categories)) {
            $results['stuckBills'] = $this->repairStuckBills($userId);
        }

        if (in_array('futureClearedTransactions', $categories)) {
            $results['futureClearedTransactions'] = $this->repairFutureClearedTransactions($userId);
        }

        // Balance recalculation should always run last (after tx cleanup)
        if (in_array('balanceDrift', $categories)) {
            $results['balanceDrift'] = $this->accountService->recalculateAllBalances($userId);
        }

        return $results;
    }

    /**
     * Find duplicate auto-generated transactions.
     * These are "next occurrence" transactions that should have been scheduled
     * but ended up as cleared duplicates of manually-entered payments.
     */
    private function findDuplicateAutoGenerated(string $userId): array {
        $accounts = $this->accountMapper->findAll($userId);
        $duplicates = [];

        foreach ($accounts as $account) {
            $accountId = $account->getId();

            // Get all auto-generated cleared transactions for this account
            $autoGen = $this->transactionMapper->findAutoGeneratedBillTransactions($accountId);

            // Exclude future-dated transactions (those are handled by futureClearedTransactions)
            $today = date('Y-m-d');
            $autoGen = array_values(array_filter($autoGen, fn($tx) => $tx->getDate() <= $today));

            // Group by (amount, created_at) to find pairs created by markPaid
            $groups = [];
            foreach ($autoGen as $tx) {
                $key = $tx->getAmount() . '|' . $tx->getCreatedAt();
                $groups[$key][] = $tx;
            }

            // Pairs where same amount+created_at have different dates = duplicate from markPaid
            foreach ($groups as $group) {
                if (count($group) < 2) {
                    continue;
                }

                // The one with the later date is the "next occurrence" duplicate
                usort($group, fn($a, $b) => strcmp($a->getDate(), $b->getDate()));
                $original = $group[0];
                for ($i = 1; $i < count($group); $i++) {
                    $duplicate = $group[$i];
                    $duplicates[] = [
                        'duplicateId' => $duplicate->getId(),
                        'originalId' => $original->getId(),
                        'amount' => (float) $duplicate->getAmount(),
                        'date' => $duplicate->getDate(),
                        'originalDate' => $original->getDate(),
                        'vendor' => $duplicate->getVendor(),
                        'accountId' => $accountId,
                        'accountName' => $account->getName(),
                    ];
                }
            }

            // Find stale "next occurrence" transactions from a previous markPaid cycle
            // that weren't cleaned up (different created_at but same bill+month)
            $this->findSameBillDuplicatesInPeriod($autoGen, $duplicates, $account->getName(), $accountId);

            // Also find auto-generated transactions that duplicate a manually-entered one
            // (same account, similar date, auto-gen notes, and a manual tx with same vendor exists)
            $this->findAutoGenDuplicatesOfManualEntries($accountId, $autoGen, $duplicates, $account->getName());
        }

        return $duplicates;
    }

    /**
     * Find auto-generated transactions for the same bill that appear multiple
     * times in the same billing period. This catches stale "next occurrence"
     * transactions from a previous markPaid that weren't cleaned up.
     */
    private function findSameBillDuplicatesInPeriod(array $autoGen, array &$duplicates, string $accountName, int $accountId): void {
        // Already-flagged IDs (from the created_at grouping above)
        $alreadyFlagged = [];
        foreach ($duplicates as $dup) {
            $alreadyFlagged[$dup['duplicateId']] = true;
        }

        // Group by bill name (from notes) + amount + month
        $groups = [];
        foreach ($autoGen as $tx) {
            $notes = $tx->getNotes() ?? '';
            if (!preg_match('/^Auto-generated from bill: (.+)$/', $notes, $m)) {
                continue;
            }
            $billName = trim($m[1]);
            $month = substr($tx->getDate(), 0, 7); // YYYY-MM
            $key = $billName . '|' . $tx->getAmount() . '|' . $month;
            $groups[$key][] = $tx;
        }

        foreach ($groups as $group) {
            if (count($group) < 2) {
                continue;
            }

            // The most recent created_at is likely the correct payment;
            // older created_at entries are stale "next occurrences" that got stuck
            usort($group, fn($a, $b) => strcmp($a->getCreatedAt(), $b->getCreatedAt()));
            $correct = end($group);

            foreach ($group as $tx) {
                if ($tx->getId() === $correct->getId()) {
                    continue;
                }
                if (isset($alreadyFlagged[$tx->getId()])) {
                    continue;
                }
                $duplicates[] = [
                    'duplicateId' => $tx->getId(),
                    'originalId' => $correct->getId(),
                    'amount' => (float) $tx->getAmount(),
                    'date' => $tx->getDate(),
                    'originalDate' => $correct->getDate(),
                    'vendor' => $tx->getVendor() ?: $correct->getVendor(),
                    'accountId' => $accountId,
                    'accountName' => $accountName,
                    'reason' => 'stale_next_occurrence',
                ];
            }
        }
    }

    /**
     * Find auto-generated transactions that duplicate manually-entered ones.
     */
    private function findAutoGenDuplicatesOfManualEntries(int $accountId, array $autoGen, array &$duplicates, string $accountName): void {
        foreach ($autoGen as $tx) {
            $vendor = $tx->getVendor();
            if (empty($vendor)) {
                continue;
            }

            // Look for a non-auto-generated transaction with the same vendor, within ±3 days
            $startDate = date('Y-m-d', strtotime($tx->getDate() . ' -3 days'));
            $endDate = date('Y-m-d', strtotime($tx->getDate() . ' +3 days'));
            $candidates = $this->transactionMapper->findByDateRange($accountId, $startDate, $endDate);

            foreach ($candidates as $candidate) {
                if ($candidate->getId() === $tx->getId()) {
                    continue;
                }
                // Skip other auto-generated ones
                $notes = $candidate->getNotes() ?? '';
                if (str_starts_with($notes, 'Auto-generated')) {
                    continue;
                }
                // Match by vendor or description
                $candVendor = mb_strtolower(trim($candidate->getVendor() ?? ''));
                $candDesc = mb_strtolower(trim($candidate->getDescription() ?? ''));
                $billName = mb_strtolower(trim($vendor));

                if ($candVendor === $billName || $candDesc === $billName
                    || str_contains($candVendor, $billName) || str_contains($candDesc, $billName)) {
                    // Check amount is in the same ballpark (within 25%)
                    $txAmt = (float) $tx->getAmount();
                    $candAmt = (float) $candidate->getAmount();
                    if ($txAmt > 0 && abs($txAmt - $candAmt) / $txAmt <= 0.25) {
                        $duplicates[] = [
                            'duplicateId' => $tx->getId(),
                            'originalId' => $candidate->getId(),
                            'amount' => $txAmt,
                            'date' => $tx->getDate(),
                            'originalDate' => $candidate->getDate(),
                            'vendor' => $vendor,
                            'accountId' => $accountId,
                            'accountName' => $accountName,
                            'reason' => 'auto_gen_duplicates_manual',
                        ];
                        break; // Only flag once per auto-gen tx
                    }
                }
            }
        }
    }

    /**
     * Find bills with stuck nextDueDate (didn't advance after payment).
     */
    private function findStuckBills(string $userId): array {
        $bills = $this->billMapper->findActive($userId);
        $stuck = [];
        $today = date('Y-m-d');

        foreach ($bills as $bill) {
            $lastPaid = $bill->getLastPaidDate();
            $nextDue = $bill->getNextDueDate();

            if (!$lastPaid || !$nextDue) {
                continue;
            }

            // Bill is stuck if it was paid but nextDueDate didn't advance.
            // For recurring bills, nextDueDate should be at least ~14 days after lastPaidDate.
            // If nextDue is within 7 days of lastPaid, it almost certainly didn't advance.
            $isStuck = false;
            $reason = '';
            $daysBetween = (strtotime($nextDue) - strtotime($lastPaid)) / 86400;
            $frequency = $bill->getFrequency();

            if ($nextDue <= $lastPaid) {
                $isStuck = true;
                $reason = 'next_due_before_paid';
            } elseif ($frequency !== 'one-time' && $daysBetween <= 7 && $lastPaid >= date('Y-m-d', strtotime('-60 days'))) {
                // For non-one-time bills, nextDue should be further out than 7 days from payment
                $isStuck = true;
                $reason = 'next_due_not_advanced';
            } elseif ($nextDue <= $today && $lastPaid >= date('Y-m-d', strtotime('-30 days'))) {
                $isStuck = true;
                $reason = 'overdue_despite_recent_payment';
            }

            if ($isStuck) {
                $stuck[] = [
                    'billId' => $bill->getId(),
                    'name' => $bill->getName(),
                    'frequency' => $bill->getFrequency(),
                    'nextDueDate' => $nextDue,
                    'lastPaidDate' => $lastPaid,
                    'reason' => $reason,
                ];
            }
        }

        return $stuck;
    }

    /**
     * Find future-dated transactions incorrectly marked as cleared.
     */
    private function findFutureClearedTransactions(string $userId): array {
        $accounts = $this->accountMapper->findAll($userId);
        $found = [];
        $today = date('Y-m-d');

        foreach ($accounts as $account) {
            $transactions = $this->transactionMapper->findByDateRange(
                $account->getId(),
                date('Y-m-d', strtotime('+1 day')),
                date('Y-m-d', strtotime('+5 years'))
            );

            foreach ($transactions as $tx) {
                $status = $tx->getStatus() ?? 'cleared';
                if ($status === 'cleared' && $tx->getDate() > $today) {
                    $found[] = [
                        'transactionId' => $tx->getId(),
                        'date' => $tx->getDate(),
                        'amount' => (float) $tx->getAmount(),
                        'type' => $tx->getType(),
                        'description' => $tx->getDescription() ?: $tx->getVendor() ?: '',
                        'accountId' => $account->getId(),
                        'accountName' => $account->getName(),
                    ];
                }
            }
        }

        return $found;
    }

    /**
     * Fix future-dated cleared transactions by setting them to scheduled
     * and reversing their balance impact.
     */
    private function repairFutureClearedTransactions(string $userId): array {
        $found = $this->findFutureClearedTransactions($userId);
        $fixed = 0;

        foreach ($found as $item) {
            try {
                $tx = $this->transactionMapper->findById($item['transactionId']);
                if (!$tx || $tx->getStatus() === 'scheduled') {
                    continue;
                }

                // Change to scheduled
                $tx->setStatus('scheduled');
                $tx->setUpdatedAt(date('Y-m-d H:i:s'));
                $this->transactionMapper->update($tx);

                // Reverse balance impact (cleared debit subtracted from balance, so add it back;
                // cleared credit added to balance, so subtract it)
                $account = $this->accountMapper->findById($tx->getAccountId());
                $currentBalance = (string) $account->getBalance();
                $amount = (string) $tx->getAmount();
                $newBalance = $tx->getType() === 'credit'
                    ? MoneyCalculator::subtract($currentBalance, $amount)
                    : MoneyCalculator::add($currentBalance, $amount);
                $this->accountMapper->updateBalance($account->getId(), $newBalance, $account->getUserId());

                $fixed++;
            } catch (\Exception $e) {
                // Skip on error
            }
        }

        return ['fixed' => $fixed, 'found' => count($found)];
    }

    /**
     * Find accounts where stored balance differs from calculated balance.
     */
    private function findBalanceDrift(string $userId): array {
        $accounts = $this->accountMapper->findAll($userId);
        $drifted = [];

        foreach ($accounts as $account) {
            $accountId = $account->getId();
            $storedBalance = (float) $account->getBalance();
            $openingBalance = (float) ($account->getOpeningBalance() ?? 0);
            $transactionNet = $this->transactionMapper->getNetChangeAll($accountId);
            $expectedBalance = $openingBalance + $transactionNet;

            $diff = abs($expectedBalance - $storedBalance);
            if ($diff > 0.005) {
                $drifted[] = [
                    'accountId' => $accountId,
                    'accountName' => $account->getName(),
                    'storedBalance' => round($storedBalance, 2),
                    'expectedBalance' => round($expectedBalance, 2),
                    'difference' => round($expectedBalance - $storedBalance, 2),
                ];
            }
        }

        return $drifted;
    }

    /**
     * Delete duplicate auto-generated transactions and return count.
     */
    private function repairDuplicateAutoGenerated(string $userId): array {
        $duplicates = $this->findDuplicateAutoGenerated($userId);
        $deleted = 0;

        // Deduplicate the list (same tx might be flagged multiple ways)
        $seen = [];
        foreach ($duplicates as $dup) {
            $id = $dup['duplicateId'];
            if (isset($seen[$id])) {
                continue;
            }
            $seen[$id] = true;

            try {
                $tx = $this->transactionMapper->findById($id);
                if ($tx) {
                    $this->transactionMapper->delete($tx);
                    $deleted++;
                }
            } catch (\Exception $e) {
                // Transaction may already be deleted, skip
            }
        }

        return ['deleted' => $deleted, 'found' => count($seen)];
    }

    /**
     * Fix bills with stuck nextDueDate by recalculating with forceAdvance.
     */
    private function repairStuckBills(string $userId): array {
        $stuck = $this->findStuckBills($userId);
        $fixed = 0;

        foreach ($stuck as $item) {
            try {
                $bill = $this->billMapper->find($item['billId'], $userId);
                $nextDue = $this->frequencyCalculator->calculateNextDueDate(
                    $bill->getFrequency(),
                    $bill->getDueDay(),
                    $bill->getDueMonth(),
                    $bill->getNextDueDate(),
                    $bill->getCustomRecurrencePattern(),
                    true // forceAdvance
                );

                // Only update if it actually advanced
                if ($nextDue !== $bill->getNextDueDate()) {
                    $bill->setNextDueDate($nextDue);
                    $this->billMapper->update($bill);
                    $fixed++;
                }
            } catch (\Exception $e) {
                // Skip bills that can't be fixed
            }
        }

        return ['fixed' => $fixed, 'found' => count($stuck)];
    }
}
